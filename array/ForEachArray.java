public class ForEachArray {
    public static void main(String[] args) {
        int a[] = { 34, 65, 78, 32, 13, 45, 67, 44 };
        for (int i : a) {
            System.out.print(i + " ");
        }
        /*
         * diffrence for loop and for each :
         * for loop :forward and backward both direction moved
         * for each :forward direction moved only
         */
    }
}
